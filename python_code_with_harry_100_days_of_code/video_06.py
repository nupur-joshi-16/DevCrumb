# Python Data Types:
# Here’s a list of commonly used data types in Python:

# 1. Numeric Types

# int → Integer (e.g., 10, -5, 100)
# float → Floating-point number (e.g., 10.5, -3.14, 2.0)
# complex → Complex numbers (e.g., 3+4j, 2-5j)

# 2. Text Type

# str → String (e.g., "Nupur", 'Python')

# 3. Boolean Type

# bool → Boolean (e.g., True, False)

# 4. Special Type

# NoneType → Represents absence of value (None)

# 5. Sequence Types

# list → Mutable ordered collection (e.g., [1, 2, 3])
# tuple → Immutable ordered collection (e.g., (1, 2, 3))
# range → Sequence of numbers (range(5) → 0,1,2,3,4)

# 6.Set Types

# set → Unordered unique values (e.g., {1, 2, 3})
# frozenset → Immutable set (frozenset({1, 2, 3}))

# 7. Mapping Type

# dict → Key-value pairs (e.g., {"name": "Nupur", "age": 41})

# 8. Binary Types (for working with binary data)

# bytes → Immutable sequence of bytes
# bytearray → Mutable sequence of bytes
# memoryview → View over existing binary data

