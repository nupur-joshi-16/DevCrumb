# Strings in Python

name = "nupur"
print(name)

# We can use a combination of single and double quotes.
fruit = 'He said, "I want to eat an apple."'
print(fruit)

# Multiline strings
fruit_2 = '''
He said,
"I would like to eat
a mango also."
'''
print(fruit_2)

# Indexing
print(name[0])
# print(name[5])  # Throws an error because index 5 is out of range
print(name[-1])

print("\n")

# Using a for loop with strings
for character in name:
    print(character)

