# Simple Calculator Exercise
# Create two number variables and apply all mathematical operators on them.

num_1 = 10
num_2 = 5

num_addition = num_1 + num_2
print(str(num_1) + " + " + str(num_2) + " = " + str(num_addition))
num_subtraction = num_1 - num_2
print(str(num_1) + " - " + str(num_2) + " = " + str(num_subtraction))
num_multiplication = num_1 * num_2
print(str(num_1) + " * " + str(num_2) + " = " + str(num_multiplication))
num_division = num_1 / num_2
print(str(num_1) + " / " + str(num_2) + " = " + str(num_division))
num_modulus = num_1 % num_2
print(str(num_1) + " % " + str(num_2) + " = " + str(num_modulus))
num_floor_div = num_1 // num_2
print(str(num_1) + " // " + str(num_2) + " = " + str(num_floor_div))
num_exponentiation = num_1 ** num_2
print(str(num_1) + " ** " + str(num_2) + " = " + str(num_exponentiation))


