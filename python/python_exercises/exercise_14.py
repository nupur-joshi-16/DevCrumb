# Calculate Area of a Circle
# Take the radius from the user and calculate the area of a circle. Use π = 3.14159

circle_radius = int(input("Please enter circle radius : "))
PI = 3.14159
circle_area = PI * circle_radius ** 2
print(f"You have enter circle radius is: {circle_radius} and calculated circle area is: {circle_area}")

